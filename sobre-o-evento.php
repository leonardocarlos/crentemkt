<h3>Sobre o Evento</h3>
<p> O evento tem como objetivo estimular os profissionais e estudantes de marketing, produtores, líderes de ministérios de marketing, voluntários na área, gerentes (moderadores e administradores) de redes sociais, organizadores de eventos, divulgadores e interessados na matéria, a difundirem e criarem obras de qualidade, com a finalidade do REINO DO SENHOR. </p>

<p>A proposta é informar, unir a classe, movimentar os recursos entre os cristãos, disseminar a cultura, propagar valores éticos e cristãos, evangelizar e projetar o marketing evangélico a nível nacional e internacional.</p>

<p>O Fórum de Marketing Evangélico é uma ferramenta para a igreja evangélica brasileira.</p>

